function html(){
  var x = document.getElementsByClassName("box-html");
  var i = 0, l = x.length;
  for( ; i < l; i++) {
    if (x[i].style.display === "none") {
      x[i].style.display = "block";
    }else {
      x[i].style.display = "none";
    }
  }
}
